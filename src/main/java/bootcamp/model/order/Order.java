package bootcamp.model.order;

import org.springframework.stereotype.Component;

@Component
public class Order {

    int id;
    int quantity;

    public Order() {

    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }
}
